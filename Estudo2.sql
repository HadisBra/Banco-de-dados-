CREATE TABLE CLIENTES (
CPF VARCHAR(11) NOT NULL,
NOME VARCHAR(40) NOT NULL,
ENDERECO VARCHAR(50) NOT NULL,
CIDADE VARCHAR(40) NOT NULL,
UF VARCHAR(2) NOT NULL,
CEP VARCHAR(9) NOT NULL,
TELEFONE VARCHAR(20),
DATA_CADASTRO DATE NOT NULL,
DATA_NASCIMENTO DATE NOT NULL,
CONSTRAINT CLIENTES_PKEY PRIMARY KEY (CPF));

CREATE TABLE FUNCIONARIOS (
CPF VARCHAR(11) NOT NULL,
NOME VARCHAR(40) NOT NULL,
ENDERECO VARCHAR(50) NOT NULL,
CIDADE VARCHAR(40) NOT NULL,
UF VARCHAR(2) NOT NULL,
CEP VARCHAR(9) NOT NULL,
TELEFONE VARCHAR(20),
DATA_ADMISSAO DATE NOT NULL,
DATA_NASCIMENTO DATE NOT NULL,
CONSTRAINT FUNCIONARIOS_PKEY PRIMARY KEY (CPF));

CREATE TABLE PRODUTOS (
CODIGO_PRODUTO INTEGER NOT NULL,
NOME VARCHAR(40) UNIQUE,
UNIDADE VARCHAR(30) NOT NULL,
QUANTIDADE INTEGER NOT NULL,
PRECO_UNITARIO INTEGER,
ESTOQUE_MINIMO INTEGER NOT NULL,
ESTOQUE_MAXIMO INTEGER NOT NULL,
CONSTRAINT PRODUTOS_PKEY PRIMARY KEY (CODIGO_PRODUTO) );

CREATE TABLE VENDAS (
CODIGO_VENDA INTEGER NOT NULL,
DATA_VENDA DATE NOT NULL,
CPF_CLIENTE VARCHAR(11) NOT NULL,
CPF_FUNCIONARIO VARCHAR(11) NOT NULL,
CONSTRAINT VENDAS_PKEY PRIMARY KEY (CODIGO_VENDA) );

CREATE TABLE VENDAS_ITENS(
CODIGO_ITEM INTEGER NOT NULL,
CODIGO_VENDA INTEGER NOT NULL,
CODIGO_PRODUTO INTEGER NOT NULL,
QUANTIDADE_ITEM INTEGER NOT NULL,
CONSTRAINT VENDAS_ITENS_PKEY PRIMARY KEY (CODIGO_ITEM));

CREATE TABLE BONUS (
CODIGO_BONUS INTEGER NOT NULL,
CPF_CLIENTE VARCHAR(11) NOT NULL,
CODIGO_VENDA INTEGER NOT NULL,
BONUS DOUBLE PRECISION NOT NULL,
CONSTRAINT BONUS_PKEY PRIMARY KEY (CODIGO_BONUS) );

CREATE TABLE COMISSOES (
CODIGO_COMISSAO INTEGER NOT NULL,
CPF_FUNCIONARIO VARCHAR(11) NOT NULL,
CODIGO_VENDA INTEGER NOT NULL,
COMISSAO DOUBLE PRECISION NOT NULL,
CONSTRAINT COMISSOES_PKEY PRIMARY KEY (CODIGO_COMISSAO));

